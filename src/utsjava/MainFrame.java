    private void btnTambahPrdkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahPrdkActionPerformed
        //Tambah produk baru pada database
        try {
            String sql = "INSERT INTO material (nama_produk, jumlah_produk, id_supplier, harga_beli, harga_jual) VALUES ('" + txtNamaPrdk.getText() +
                    "', '" + Integer.parseInt(txtJumlahPrdk.getText()) + "', '" + Integer.parseInt(txtIdSup.getText()) + "', '" +
                    Integer.parseInt(txtHargaBeli.getText()) + "', '" + Integer.parseInt(txtHargaJual.getText()) + "')";
            java.sql.Connection conn = (Connection) Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan data berhasil");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        Kosong();
        LoadTable();
    }//GEN-LAST:event_btnTambahPrdkActionPerformed

