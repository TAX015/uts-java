    private void LoadTable() {
        //Tampilan model table
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID Material");
        model.addColumn("Nama");
        model.addColumn("Jumlah");
        model.addColumn("ID Supplier");
        model.addColumn("Harga Beli");
        model.addColumn("Harga Jual");  
        
        //Menampilkan data ke dalam table
        try {
            int no = 1;
            String sql = "SELECT * FROM material";
            java.sql.Connection conn = (Connection) Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()) {
                model.addRow(new Object[]{no++, res.getInt(1), res.getString(2), res.getInt(3), res.getInt(4), res.getInt(5), res.getInt(6)});
            }
            
            tableMat.setModel(model);
        } catch(Exception e) {
        }
    }
    
