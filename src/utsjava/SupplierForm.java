    private void btnTambahSupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahSupActionPerformed
        //Tambah data supplier pada database
        try {
            String sql = "INSERT INTO supplier (nama_supplier, nama_sales, telp_supplier) VALUES ('" + txtNamaSup.getText() +
                    "', '" + txtNamaSales.getText() + "', '" + txtTelpSup.getText() + "')";
            java.sql.Connection conn = (Connection) Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Penyimpanan data berhasil");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
        
        Kosong();
        LoadTable();
    }//GEN-LAST:event_btnTambahSupActionPerformed

    private void tableSupMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableSupMouseClicked
        //Tampilkan data ke dalam form ketika data pada table di click
        int baris = tableSup.rowAtPoint(evt.getPoint());
        String id = tableSup.getValueAt(baris, 1).toString();
        txtIdSup.setText(id);
        String namaSup = tableSup.getValueAt(baris, 2).toString();
        txtNamaSup.setText(namaSup);
        String namaSales = tableSup.getValueAt(baris, 3).toString();
        txtNamaSales.setText(namaSales);
        String telp = tableSup.getValueAt(baris, 4).toString();
        txtTelpSup.setText(telp);
        
    }//GEN-LAST:event_tableSupMouseClicked

    private void btnEditSupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditSupActionPerformed
        //Edit data pada database supplier
        try {
            String sql = "UPDATE supplier SET nama_supplier='" + txtNamaSup.getText() + "', nama_sales='" + txtNamaSales.getText() + "', telp_supplier='" +
                    txtTelpSup.getText() + "' WHERE id_supplier='" + Integer.parseInt(txtIdSup.getText()) + "'";
            java.sql.Connection conn = (Connection) Koneksi.configDB();
            java.sql.PreparedStatement pst = conn.prepareStatement(sql);
            pst.execute();
            JOptionPane.showMessageDialog(null, "Data berhasil di edit");
        } catch(Exception e) {
            JOptionPane.showMessageDialog(null, "Update gagal " + e.getMessage());
        }
        
        Kosong();
        LoadTable();
    }//GEN-LAST:event_btnEditSupActionPerformed

