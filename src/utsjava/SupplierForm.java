    private void LoadTable() {
        //Tampilan model table
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("No");
        model.addColumn("ID Supplier");
        model.addColumn("Nama");
        model.addColumn("Sales");
        model.addColumn("telp");
        
        //Menampilkan data ke dalam table
        try {
            int no = 1;
            String sql = "SELECT * FROM supplier";
            java.sql.Connection conn = (Connection) Koneksi.configDB();
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet res = stm.executeQuery(sql);
            
            while(res.next()) {
                model.addRow(new Object[]{no++, res.getInt(1), res.getString(2), res.getString(3), res.getString(4)});
            }
            
            tableSup.setModel(model);
        } catch(Exception e) {
        }
    }
    
